#include<stdio.h>

int main()
{
	int x;
	scanf("%d",&x);
	if(x<10)
	{
		printf("1\n");
		printf("%d\n",x);
		printf("%d\n",x);
	}
	if((x>=10)&&(x<100))
	{
		printf("2\n");
		printf("%d %d\n",(x-x%10)/10,x%10);
		printf("%d%d\n",x%10,(x-x%10)/10);
	}
	if((x>=100)&&(x<1000))	
	{
		printf("3\n");
		printf("%d %d %d\n",(x-x%100)/100,(x%100-x%100%10)/10,x%100%10);
		printf("%d%d%d\n",x%100%10,(x%100-x%100%10)/10,(x-x%100)/100);
	}
	if((x>=1000)&&(x<10000))
	{
		printf("4\n");
		printf("%d %d %d %d\n",(x-x%1000)/1000,(x%1000-x%100)/100,((x%100)-(x%10))/10,x%10);
		printf("%d%d%d%d\n",x%10,((x%100)-(x%10))/10,(x%1000-x%100)/100,(x-x%1000)/1000);
	}
	if((x>=10000)&&(x<100000))
	{
		printf("5\n");
		printf("%d %d %d %d %d\n",(x-x%10000)/10000,(x%10000-x%1000)/1000,(x%1000-x%100)/100,(x%100-x%10)/10,x%10);
		printf("%d%d%d%d%d\n",x%10,(x%100-x%10)/10,(x%1000-x%100)/100,(x%10000-x%1000)/1000,(x-x%10000)/10000);
	}
	return 0;
}
